    private void addThangActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addThangActionPerformed
        String s = "Ngày"; int x = 29;
        if(!"Ngày".equals(addNgay.getSelectedItem()))
            s = (String) addNgay.getSelectedItem();
        if ("02".equals(addThang.getSelectedItem())) {
            if(!"Năm".equals(addNam.getSelectedItem()))
            x = namNhuan(Integer.parseInt((String) addNam.getSelectedItem())) ? 29 : 28;
                addNgay.removeAllItems();
                addNgay.addItem("Ngày");
                
                for (int i = 1; i <= x; i++) {
                    String dayStr = (i < 10) ? "0" + String.valueOf(i) : String.valueOf(i);
                    addNgay.addItem(dayStr);
                }
            
        }
        var t31 = Arrays.asList("01", "03", "05", "07", "08", "10", "12");
        if (t31.contains(addThang.getSelectedItem())) {
            addNgay.removeAllItems();
            addNgay.addItem("Ngày");    
            for (int i = 1; i <= (x = 31); i++) {
                String dayStr = (i < 10) ? "0" + String.valueOf(i) : String.valueOf(i);
                addNgay.addItem(dayStr);
            }
        }
        var t30 = Arrays.asList("04", "06", "09", "11");
        if (t30.contains(addThang.getSelectedItem())) {
            addNgay.removeAllItems();
            addNgay.addItem("Ngày");    
            for (int i = 1; i <= (x = 30); i++) {
                String dayStr = (i < 10) ? "0" + String.valueOf(i) : String.valueOf(i);
                addNgay.addItem(dayStr);
            }
        }
        if (!"Ngày".equals(s) && Integer.parseInt(s) <= x) addNgay.setSelectedItem(s);
    }//GEN-LAST:event_addThangActionPerformed

