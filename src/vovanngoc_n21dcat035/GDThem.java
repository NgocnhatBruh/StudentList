    private void NutLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_NutLuuActionPerformed
        String maChuan = "[nbNB]\\d{2}[A-Za-z]{4}\\d{3}";
        String sdtChuan = "[0-9]{10}";
        String mailChuan = "^[A-Za-z0-9+_.-]+@[A-Za-z0-9.-]+$";
        if("Nhập Mã Sinh Viên".equals(addMaSV.getText()) || "Nhập Họ Và Tên".equals(addHoTen.getText()) 
                || "Ngày".equals(addNgay.getSelectedItem()) || "Tháng".equals(addThang.getSelectedItem()) 
                || "Năm".equals(addNam.getSelectedItem()) || (!addGTNu.isSelected() && !addGTNam.isSelected())
            || "Nhập Số Điện Thoại".equals(addSDT.getText()) || "Nhập Mail".equals(addMail.getText())){
            JOptionPane.showMessageDialog(this, "Không để trống thông tin!!!");
        }
        else if(!addMaSV.getText().matches(maChuan)){
            JOptionPane.showMessageDialog(this, "Mã sinh Viên không đúng định dạng!!!");
        }else if(!addSDT.getText().matches(sdtChuan)){
            JOptionPane.showMessageDialog(this, "Số điện thoại không đúng định dạng!!!");
        }else if(!addMail.getText().matches(mailChuan)){
            JOptionPane.showMessageDialog(this, "Mail không đúng định dạng!!!");
        }
        else{
            String ns = addNam.getSelectedItem() + "-" + addThang.getSelectedItem() + "-" +addNgay.getSelectedItem();
            byte gt;
            if(addGTNam.isSelected()) gt = 1;
            else gt = 0;
            XuLySQL.luuSV(addMaSV.getText(), addHoTen.getText(), ns, gt, addSDT.getText(), addMail.getText());
            if(XuLySQL.loiLuu == 1){
                JOptionPane.showMessageDialog(this, "Mã sinh viên " + addMaSV.getText() + " đã tồn tại!!!");
            }else if(XuLySQL.loiLuu == 2){
                JOptionPane.showMessageDialog(this, "Số điện thoại " + addSDT.getText() + " đã tồn tại!!!");
            }else if(XuLySQL.loiLuu == 3){
                JOptionPane.showMessageDialog(this, "Mail " + addMail.getText() + " đã tồn tại!!!");
            }
            else{
                JOptionPane.showMessageDialog(this, "Thêm thành công!!!");
                dispose();
                addMaSV.setText(""); addHoTen.setText(""); 
                addNgay.setSelectedItem("");; addThang.setSelectedItem(""); addNam.setSelectedItem("");
                addGTNam.setSelected(false); addGTNu.setSelected(false); addSDT.setText(""); addMail.setText("");
            }
        }
    }//GEN-LAST:event_NutLuuActionPerformed

