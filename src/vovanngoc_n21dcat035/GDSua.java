    private void editThangActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editThangActionPerformed
        String s = "Ngày"; int x = 29;
        if(!"Ngày".equals(editNgay.getSelectedItem()))
        s = (String) editNgay.getSelectedItem();
        if ("02".equals(editThang.getSelectedItem())) {
            if(!"Năm".equals(editNam.getSelectedItem()))
            x = namNhuan(Integer.parseInt((String) editNam.getSelectedItem())) ? 29 : 28;
            editNgay.removeAllItems();
            editNgay.addItem("Ngày");

            for (int i = 1; i <= x; i++) {
                String dayStr = (i < 10) ? "0" + String.valueOf(i) : String.valueOf(i);
                editNgay.addItem(dayStr);
            }

        }
        var t31 = Arrays.asList("01", "03", "05", "07", "08", "10", "12");
        if (t31.contains(editThang.getSelectedItem())) {
            editNgay.removeAllItems();
            editNgay.addItem("Ngày");
            for (int i = 1; i <= (x = 31); i++) {
                String dayStr = (i < 10) ? "0" + String.valueOf(i) : String.valueOf(i);
                editNgay.addItem(dayStr);
            }
        }
        var t30 = Arrays.asList("04", "06", "09", "11");
        if (t30.contains(editThang.getSelectedItem())) {
            editNgay.removeAllItems();
            editNgay.addItem("Ngày");
            for (int i = 1; i <= (x = 30); i++) {
                String dayStr = (i < 10) ? "0" + String.valueOf(i) : String.valueOf(i);
                editNgay.addItem(dayStr);
            }
        }
        if (!"Ngày".equals(s) && Integer.parseInt(s) <= x) editNgay.setSelectedItem(s);
    }//GEN-LAST:event_editThangActionPerformed

