    private void NutSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_NutSuaActionPerformed
String sdtChuan = "[0-9]{10}";
        String mailChuan = "^[A-Za-z0-9+_.-]+@[A-Za-z0-9.-]+$";
        if("Nhập Họ Và Tên".equals(editHoTen.getText()) || "Ngày".equals(editNgay.getSelectedItem()) 
                || "Tháng".equals(editThang.getSelectedItem()) || "Năm".equals(editNam.getSelectedItem()) 
                || (!editGTNu.isSelected() && !editGTNam.isSelected()) || "Nhập Số Điện Thoại".equals(editSDT.getText()) 
                || "Nhập Mail".equals(editMail.getText())){
            JOptionPane.showMessageDialog(this, "Không để trống thông tin!!!");
        }else if(!editSDT.getText().matches(sdtChuan)){
            JOptionPane.showMessageDialog(this, "Số điện thoại không đúng định dạng!!!");
        }else if(!editMail.getText().matches(mailChuan)){
            JOptionPane.showMessageDialog(this, "Mail không đúng định dạng!!!");
        }
        else{
            String ns = editNam.getSelectedItem() + "-" + editThang.getSelectedItem() + "-" + editNgay.getSelectedItem();
            byte gt;
            if(editGTNam.isSelected()) gt = 1;
            else gt = 0;
            XuLySQL.suaSV(editMaSV.getText().substring(14), editHoTen.getText(), ns, gt, editSDT.getText(), editMail.getText());
            if(XuLySQL.loiSua == 1){
                JOptionPane.showMessageDialog(this, "Số điện thoại " + editSDT.getText() + " đã tồn tại!!!");
            }else if(XuLySQL.loiSua == 2){
                JOptionPane.showMessageDialog(this, "Mail " + editMail.getText() + " đã tồn tại!!!");
            }
            else{
                JOptionPane.showMessageDialog(this, "Sửa thành công!!!");
                dispose();
                editHoTen.setText("");
                editNgay.setSelectedItem("");; editThang.setSelectedItem(""); editNam.setSelectedItem("");
                editGTNam.setSelected(false); editGTNu.setSelected(false); editSDT.setText(""); editMail.setText("");
            }
        }
    }//GEN-LAST:event_NutSuaActionPerformed

