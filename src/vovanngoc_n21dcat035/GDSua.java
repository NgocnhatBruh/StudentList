    public void setThongTin(int row) {
        String ns = (String) GiaoDienChinh.dtm.getValueAt(row, 2);
        String[] ngay = ns.split("-");
        editMaSV.setText("Mã Sinh Viên: " + (String) GiaoDienChinh.dtm.getValueAt(row, 0));
        editHoTen.setText((String) GiaoDienChinh.dtm.getValueAt(row, 1));
        editNgay.setSelectedItem(ngay[2]);
        editThang.setSelectedItem(ngay[1]);
        editNam.setSelectedItem(ngay[0]);
        if((Byte) GiaoDienChinh.dtm.getValueAt(row, 3) == 1) {
            editGTNam.setSelected(true);
            editGTNu.setSelected(false);
        } else {
            editGTNu.setSelected(true);
            editGTNam.setSelected(false);
        }
        editSDT.setText((String) GiaoDienChinh.dtm.getValueAt(row, 4));
        editMail.setText((String) GiaoDienChinh.dtm.getValueAt(row, 5));
    }
